// ─── Помощни функции за изграждане на тялото на документа ───
const P = "margin:0 0 10px;line-height:1.6;";
const H = "font-family:'Fraunces',serif;text-align:center;font-size:18px;font-weight:700;margin:0 0 4px;text-transform:uppercase;";

function head(extra = ""): string {
  return `<p style="${P}"><strong>{{Фирма.Име}}</strong>, ЕИК {{Фирма.ЕИК}}, {{Фирма.Адрес}}, {{Фирма.Град}}, представлявано от {{Фирма.Управител}}${extra}</p>`;
}
function meta(): string {
  return `<p style="text-align:center;color:#555;font-size:13px;margin:0 0 18px;">№ {{Документ.Номер}} · {{Място}}, {{ТекущаДата}}</p>`;
}
function signatures(left = "Възложител", right = "Изпълнител"): string {
  return `<table style="width:100%;margin-top:48px;border-collapse:collapse;"><tr>
<td style="width:50%;text-align:center;padding-top:6px;border-top:1px solid #16201C;">${left}: ............................</td>
<td style="width:6%;"></td>
<td style="width:44%;text-align:center;padding-top:6px;border-top:1px solid #16201C;">${right}: ............................</td></tr></table>`;
}

// Помощник за договори — пълна професионална структура
function contractDoc(opts: { title: string; preamble: string; other: string; clauses: { h: string; b: string }[]; left?: string; right?: string }): string {
  const clauses = opts.clauses.map((c, i) => `<p style="${P}"><strong>Чл. ${i + 1}. ${c.h}</strong> ${c.b}</p>`).join("\n");
  return `
    <h1 style="${H}">${opts.title}</h1>
    ${meta()}
    <p style="${P}">Днес, {{ТекущаДата}} в {{Място}}, между долуподписаните страни:</p>
    <p style="${P}"><strong>1. {{Фирма.Име}}</strong>, ЕИК {{Фирма.ЕИК}}, ДДС № {{Фирма.ДДС}}, със седалище и адрес на управление {{Фирма.Адрес}}, {{Фирма.Град}}, представлявано от {{Фирма.Управител}}, наричано по-долу <strong>„${opts.left ?? "ВЪЗЛОЖИТЕЛ"}"</strong>, и</p>
    <p style="${P}"><strong>2. ${opts.other}</strong>, наричан/о по-долу <strong>„${opts.right ?? "ИЗПЪЛНИТЕЛ"}"</strong>,</p>
    <p style="${P}">${opts.preamble}</p>
    <p style="text-align:center;font-weight:700;margin:12px 0;">СЕ СКЛЮЧИ НАСТОЯЩИЯТ ДОГОВОР ЗА СЛЕДНОТО:</p>
    ${clauses}
    <p style="${P}"><strong>Чл. ${opts.clauses.length + 1}. Заключителни разпоредби.</strong> За неуредените въпроси се прилагат разпоредбите на действащото българско законодателство. Всички спорове се решават чрез преговори, а при непостигане на съгласие — от компетентния съд. Договорът се състави и подписа в два еднообразни екземпляра — по един за всяка от страните.</p>
    ${signatures(opts.left ?? "ВЪЗЛОЖИТЕЛ", opts.right ?? "ИЗПЪЛНИТЕЛ")}
  `;
}

// Примерен (редактируем) текст — визуално обозначен, за да се вижда какво да съдържа полето
function ex(text: string): string {
  return `<span style="color:#8a8a80;font-style:italic;">(напр. ${text})</span>`;
}

